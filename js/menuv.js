/* Set the width of the sidebar to 250px and the left margin of the page content to 250px */
function ObrirMenu() {
    document.getElementById("VMenu").style.width = "250px";
    document.getElementById("BMenu").style.marginLeft = "250px";
  }
  
  /* Set the width of the sidebar to 0 and the left margin of the page content to 0 */
  function TancarMenu() {
    document.getElementById("VMenu").style.width = "0";
    document.getElementById("BMenu").style.marginLeft = "0";
  }


  